int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int z;

	z = n - 1;
	i = 0;
	while ((s1[i] != '\0') && (s2[i] != '\0') && (i < z))
	{
		if (s1[i] != s2[i])
			return ((s1[i] - 48) - (s2[i] - 48));
		i++;
	}
	return ((s1[i] - 48) - (s2[i] - 48));
}
