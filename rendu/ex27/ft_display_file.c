#include <unistd.h>
#include <fcntl.h>

void	disp_file(int fd)
{
	char	cara;

	while (read(fd, &cara, 1) != 0)
		write(1, &cara, 1);
}

int		main(int argc, char **argv)
{
	int	fd;

	if (argc != 2)
	{
		if (argc > 2)
			write(2, "Too many arguments.\n", 20);
		if (argc < 2)
			write(2, "File name missing.\n", 19);
		return (1);
	}
	fd = open(argv[1], O_RDONLY);
	disp_file(fd);
	close(fd);
	return (0);
}